#include "calc.h"

uint8 u8fn_sum(uint8 a, uint8 b){
	return (a + b);
}
uint8 u8fn_sub(uint8 a, uint8 b){
	return (a - b);
}
uint8 u8fn_mul(uint8 a, uint8 b){
	return (a * b);
}
uint8 u8fn_div(uint8 a, uint8 b){
	return (a / b);
}
